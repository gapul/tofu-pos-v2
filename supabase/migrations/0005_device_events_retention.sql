-- Tofu POS — device_events の retention 運用ガイド
-- 仕様書/コード参照: lib/core/transport/supabase_transport.dart
--
-- このマイグレーション自体は **テーブル定義を変更しない**。
-- 0004_device_events.sql で `inserted_at` の index は作成済みなので、
-- ここでは「いつ、どう削除するか」の運用ノートだけを残す。
--
-- ============================================================================
-- 学祭1日運用の場合
-- ============================================================================
-- 自然蓄積で問題ない。終演後にテーブルごと TRUNCATE してもよい:
--   truncate table public.device_events;
--
-- ============================================================================
-- 長期運用の場合（30日 retention）
-- ============================================================================
-- 以下を週次 cron などで実行する。idx_device_events_shop_inserted を使うので
-- 大量行があっても比較的速い:
--
--   delete from public.device_events
--    where inserted_at < now() - interval '30 days';
--
-- ============================================================================
-- pg_cron を使うなら（Supabase の有償プランで有効化可能）
-- ============================================================================
--
--   select cron.schedule(
--     'device_events_retention',
--     '0 3 * * *',  -- 毎日 03:00 UTC
--     $$ delete from public.device_events
--          where inserted_at < now() - interval '30 days'; $$
--   );
--
-- ============================================================================
-- アプリ側の修正は不要。`SupabaseTransport` は永続データではなく
-- 短命シグナリングだけ扱うため、過去行が消えても業務影響なし。
