/// 呼び出し端末側のステータス（仕様書 §5.6）。
enum CallingStatus {
  /// 調理待ち（会計確定済み、キッチン提供完了前）。
  ///
  /// 呼び出し端末 UI 上は「呼び出し前」リストに混ぜて表示するが、自動
  /// ポップアップ（整理券大画面）は **発火しない**。CallNumberEvent
  /// （= 提供完了がレジ経由で転送されてきた瞬間）で [pending] に昇格
  /// したタイミングで初めてポップアップ対象となる。
  awaitingKitchen,

  /// 呼び出し前（料理完成、お客様呼び出し可能）。
  pending,

  /// 呼び出し済み
  called,

  /// 受取完了（お客様が商品を受け取った）
  pickedUp,

  /// 取消（レジから取消通知を受けた）
  cancelled,
}
